    public void carregaDados() {
        DefaultTableModel tabela = (DefaultTableModel) TB_Livros.getModel();

        livrosDAO DAO = new livrosDAO();
        tabela.setNumRows(0);

        for (livros l : DAO.listar()) {
            tabela.addRow(new Object[]{
                l.getId_livro(),
                l.getTitulo(),
                l.getEditora(),
                l.getAutor(),
                l.getAno()
            });
        }
    }

    public void pesquisaDados() {
        DefaultTableModel tabela = (DefaultTableModel) TB_Livros.getModel();
        livrosDAO dao = new livrosDAO();
        tabela.setNumRows(0);

        for (livros l : dao.pesquisar(TF_Pesquisar.getText())) {
            tabela.addRow(new Object[]{
                l.getId_livro(),
                l.getTitulo(),
                l.getEditora(),
                l.getAutor(),
                l.getAno()
            });
        }
    }

