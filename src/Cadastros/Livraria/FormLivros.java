    private void TB_LivrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TB_LivrosMouseClicked

     String comboAutor = TB_Livros.getValueAt(TB_Livros.getSelectedRow(), 3).toString();
        String comboEditora = TB_Livros.getValueAt(TB_Livros.getSelectedRow(), 2).toString();
        
        
        TF_Codigo.setText(TB_Livros.getValueAt(TB_Livros.getSelectedRow(), 0).toString());
        TF_Ano.setText(TB_Livros.getValueAt(TB_Livros.getSelectedRow(), 4).toString());
        TF_Titulo.setText(TB_Livros.getValueAt(TB_Livros.getSelectedRow(), 1).toString());

        
        for (int i = 0; i < CB_Editoras.getItemCount(); i++) {
            if (CB_Editoras.getItemAt(i).toString().equalsIgnoreCase(comboEditora)) {
                CB_Editoras.setSelectedIndex(i);
            }
        }

       
        for (int i = 0; i < CB_Autor.getItemCount(); i++) {
            if (CB_Autor.getItemAt(i).toString().equalsIgnoreCase(comboAutor)) {
                CB_Autor.setSelectedIndex(i);
            }
        }

    }//GEN-LAST:event_TB_LivrosMouseClicked
        
     
     
